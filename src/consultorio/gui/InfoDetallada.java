    private void llenaTextAreas(ArrayList<String> dP){
            String sexo = ("0".equals(dP.get(4)))?"F":"M";
            String diabetes = ("0".equals(dP.get(12)))?"No":"Si";
            String hipertension = ("0".equals(dP.get(13)))?"No":"Si";
            String tabaco = ("0".equals(dP.get(14)))?"No":"Si";
            String alcohol =("0".equals(dP.get(15)))?"No":"Si";
            String formatPersonales = "Nombre: "+dP.get(1)+" "+dP.get(2)+" "+dP.get(3)
            +"\nNacimiento: "+dP.get(10)+"\tEdad: "+dP.get(11)+" a√±os  Sexo: "+sexo+"\nEstado Civil: "+dP.get(5)+"\t"
            + "Ocupacion: "+dP.get(6)+"\nTelefono: "+dP.get(7)+"\tCelular: "+dP.get(8)+"\n"
            + "Email: "+dP.get(9);
            String formatPatologicos = "Diabetes:"+diabetes+"\nHiperTension:"+hipertension+"\n"
                    + "Fuma:"+tabaco+"\nBebe:"+alcohol+"\nOtros:"+dP.get(16);
            String formatNoPatologicos = "Peso: "+dP.get(22)+"kg \nTalla: "+dP.get(23)+"\nTipo de Sangre: "+dP.get(24)+"\nFrecuencia Cardiaca: "+dP.get(25)
                    +"\nTension Arterial: "+dP.get(26)+"\nFrecuencia Respiratoria: "+dP.get(27)+"\nOtros: "+dP.get(28);
            String formatAlergias = "Medicamentos: "+dP.get(17)+"\nSustancias: "+dP.get(18)+"\nAlimentos: "+dP.get(19)
                    + "\nOtras: "+dP.get(20);
            
            jTAGral.setText(formatPersonales);
            jTAPat.setText(formatPatologicos);
            jTANoPat.setText(formatNoPatologicos);
            jTAAlergias.setText(formatAlergias);
    }


    
    
    
