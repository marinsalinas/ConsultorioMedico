    private void validame() throws Exception{
        String[] datos = {jTFNombre.getText(), jTFAp1.getText(), jTFAp2.getText(), 
            jTFEstCiv.getText(), jTFOcupacion.getText(), jTFTel.getText(), jTFCel.getText(),
            jTFEmail.getText(), jTFAnio.getText(), otrosPatTField.getText(), mediTextArea.getText(),
            sustanciasTextArea.getText(), alimentosTextArea.getText(), otrasAlergiasTField.getText(), 
            gineTField.getText(), pesoTField.getText(), tallaTField.getText(), sangreTField.getText(),
            frecCardTField.getText(), tenArtTField.getText(), frecRespTField.getText(), otrosNoPatTFeld.getText() };
        boolean sino = true;
        for (int i = 0; i <= datos.length - 1; i++) {
            if (datos[i].isEmpty()) {
                System.err.println("Algunos campos estan vacios");
                //sino = false;
                throw new Exception("Algun Campo de Texto se encuentra Vacio\n"
                        + "Escriba N/A si no aplica\nEn caso de que sea numerico '0'");
            }
        }
} 
 
