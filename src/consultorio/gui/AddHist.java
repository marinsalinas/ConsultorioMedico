    private void jButtonAcepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAcepActionPerformed
        // TODO add your handling code here:
       try{ 
        Historial hist = new Historial();
        hist.setId_paciente(getId());
        hist.setPadActual(jTFPadAct.getText());
        hist.setExLab(jTFExLab.getText());
        hist.setExImg(jTFExImg.getText());
        hist.setTratamiento(jTATratamiento.getText());
        hist.setComentarios(jTextArea1.getText());
        hist.setSeguimiento(new Fecha(
                Integer.parseInt(String.valueOf(jCBdia.getSelectedItem())),
                Integer.parseInt(String.valueOf(jCBmes.getSelectedItem())), 
                Integer.parseInt(jTFanio.getText())));
        ConexionDB.altaHistorial(hist);
        this.dispose();
       }catch(IllegalArgumentException e){
           JOptionPane.showMessageDialog(null, e, "MediHist v0.1", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_jButtonAcepActionPerformed

